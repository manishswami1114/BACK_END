function add(num1, num2) {
    return num1 + num2;
}
function Sub(num1, num2) {
    return num1 - num2;
}
function mul(num1, num2) {
    return num1 * num2;
}
function div(num1, num2) {
    return num1 / num2;
}
module.exports.add = add;
module.exports.Sub= Sub;
module.exports.mul= mul;
module.exports.div= div;
